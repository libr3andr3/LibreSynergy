class H(BaseHTTPRequestHandler):
    def _send(self, code, body, ctype="application/json", cookie=None):
        data = body if isinstance(body, bytes) else json.dumps(body).encode()
        self.send_response(code)
        self.send_header("Content-Type", ctype)
        self.send_header("Content-Length", str(len(data)))
        self.send_header("Cache-Control", "no-store")
        if cookie:
            self.send_header("Set-Cookie", cookie)
        self.end_headers()
        self.wfile.write(data)

    def _admin(self):
        groups = self.headers.get("X-authentik-groups", "")
        if ADMIN_GROUP not in [g.strip() for g in groups.split("|")]:
            self._send(403, {"error": f"requires group '{ADMIN_GROUP}'"})
            return False
        return True

    def do_GET(self):
        path = urllib.parse.urlparse(self.path).path
        if path == "/healthz":
            return self._send(200, {"ok": True})
        if not self._admin(): return
        if path == "/brand-favicon":
            try: return self._send(200, open(os.path.join(WWW, "brand", "favicon.ico"), "rb").read(), "image/x-icon")
            except Exception: return self._send(404, {"error": "no favicon"})
        if path == "/api/state":
            lang, _ = i18n.resolve(self)
            v = env_read()
            checks = [(sid, i18n.t(lang, "svc." + sid), port_check(p, hp)) for sid, p, hp in SERVICES]
            hs = {sid: ok for sid, _label, ok in checks}
            health = [[label, ok] for _sid, label, ok in checks]
            stripe_on = False
            btc_on = False
            try:
                with urllib.request.urlopen("http://127.0.0.1:9092/healthz", timeout=3) as r:
                    j = json.load(r); stripe_on = j.get("stripe", False); btc_on = j.get("btcpay", False)
            except Exception:
                btc_on = False
            launch = [
                [i18n.t(lang, "launch.stripe"), stripe_on],
                [i18n.t(lang, "launch.btcpay"), btc_on],
                [i18n.t(lang, "launch.owncast"), hs.get("owncast", False)],
                [i18n.t(lang, "launch.sso"), hs.get("sso", False)],
                [i18n.t(lang, "launch.chat"), hs.get("matrix", False)],
            ]
            return self._send(200, {
                "user": self.headers.get("X-authentik-username", ""),
                "brand": {k: v.get(k, "") for k in BRAND_KEYS + ["LS_BASE_DOMAIN"]},
                "sponsors": jload(SPONSORS, []),
                "banner": jload(BANNER, {}),
                "health": health, "launch": launch})
        if path == "/":
            lang, set_cookie = i18n.resolve(self)
            v = env_read()
            page = render_page(lang, v)
            cookie = f"ls_lang={lang}; Path=/; Max-Age=31536000; SameSite=Lax" if set_cookie else None
            return self._send(200, page.encode(), "text/html; charset=utf-8", cookie=cookie)
        self._send(404, {"error": "not found"})

    def do_POST(self):
        if not self._admin(): return
        try:
            body = json.loads(self.rfile.read(int(self.headers.get("Content-Length", 0)) or 0) or b"{}")
        except Exception:
            return self._send(400, {"error": "bad json"})
        with _lock:
            if self.path == "/api/branding":
                updates = {k: str(body.get(k, "")).strip() for k in BRAND_KEYS if k in body}
                for ck in ("LS_COLOR_BRAND", "LS_COLOR_ACCENT", "LS_COLOR_INK"):
                    if ck in updates and not re.fullmatch(r"#[0-9a-fA-F]{6}", updates[ck]):
                        return self._send(400, {"error": f"{ck} must be #rrggbb"})
                env_write(updates)
                tokens_css(env_read())
                open(FLAG, "w").write(str(time.time()))   # host cron runs apply-branding.sh
                return self._send(200, {"ok": True})
            if self.path == "/api/sponsors":
                if not body.get("name") or not body.get("url"):
                    return self._send(400, {"error": "name and url required"})
                lst = jload(SPONSORS, [])
                lst.append({"name": body["name"][:80], "url": body["url"][:300], "logo": body.get("logo", "")[:300]})
                jsave(SPONSORS, lst)
                return self._send(200, {"ok": True, "sponsors": lst})
            if self.path == "/api/banner":
                jsave(BANNER, {"text": str(body.get("text", ""))[:300], "url": str(body.get("url", ""))[:300]})
                return self._send(200, {"ok": True})
            if self.path == "/api/announce":
                text = str(body.get("text", "")).strip()
                if not text: return self._send(400, {"error": "empty"})
                ok, info = matrix_announce(text)
                return self._send(200 if ok else 502, {"ok": ok, "id": info})
        self._send(404, {"error": "not found"})

    def do_DELETE(self):
        if not self._admin(): return
        if self.path.startswith("/api/sponsors"):
            q = urllib.parse.parse_qs(urllib.parse.urlparse(self.path).query)
            i = int(q.get("i", ["-1"])[0])
            with _lock:
                lst = jload(SPONSORS, [])
