def env_read():
    vals = {}
    try:
        for line in open(LS_ENV):
            m = re.match(r'^([A-Z_]+)=(?:"([^"]*)"|(.*))$', line.strip())
            if m: vals[m.group(1)] = m.group(2) if m.group(2) is not None else m.group(3)
    except FileNotFoundError:
        pass
    return vals

def env_write(updates):
    lines = open(LS_ENV).read().splitlines(keepends=False)
    out = []
    seen = set()
    for line in lines:
        m = re.match(r'^([A-Z_]+)=', line)
        if m and m.group(1) in updates:
            out.append(f'{m.group(1)}="{updates[m.group(1)]}"')
            seen.add(m.group(1))
        else:
            out.append(line)
    for k, v in updates.items():
        if k not in seen: out.append(f'{k}="{v}"')
    open(LS_ENV, "w").write("\n".join(out) + "\n")

